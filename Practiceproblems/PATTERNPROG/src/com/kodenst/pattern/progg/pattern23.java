package com.kodenst.pattern.progg;

public class pattern23 {

	public static void main(String[] args) {
		for (int i=1; i<=5; i++) {
			System.out.print("*");
		}
		System.out.println( );
		for (int a=1; a<=1; a++) {
			System.out.println("**  *");
		}
		for (int b=1; b<=5; b++) {
			System.out.print("*");
		}
		System.out.println( );
	} 
}